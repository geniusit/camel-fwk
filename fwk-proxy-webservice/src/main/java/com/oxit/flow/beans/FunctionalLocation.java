package com.oxit.flow.beans;

public class FunctionalLocation {
	
	private String processTypeName;
	private String documentTypeName;
	private String senderID;
	private String documentID;
	private String businessTags;
	private String payloadSize;
	private String serviceName;
	
	/**
	 * @return the processTypeName
	 */
	public String getProcessTypeName() {
		return processTypeName;
	}
	/**
	 * @param processTypeName the processTypeName to set
	 */
	public void setProcessTypeName(String processTypeName) {
		this.processTypeName = processTypeName;
	}
	/**
	 * @return the documentTypeName
	 */
	public String getDocumentTypeName() {
		return documentTypeName;
	}
	/**
	 * @param documentTypeName the documentTypeName to set
	 */
	public void setDocumentTypeName(String documentTypeName) {
		this.documentTypeName = documentTypeName;
	}
	/**
	 * @return the senderID
	 */
	public String getSenderID() {
		return senderID;
	}
	/**
	 * @param senderID the senderID to set
	 */
	public void setSenderID(String senderID) {
		this.senderID = senderID;
	}
	/**
	 * @return the documentID
	 */
	public String getDocumentID() {
		return documentID;
	}
	/**
	 * @param documentID the documentID to set
	 */
	public void setDocumentID(String documentID) {
		this.documentID = documentID;
	}
	/**
	 * @return the businessTags
	 */
	public String getBusinessTags() {
		return businessTags;
	}
	/**
	 * @param businessTags the businessTags to set
	 */
	public void setBusinessTags(String businessTags) {
		this.businessTags = businessTags;
	}
	/**
	 * @return the payloadSize
	 */
	public String getPayloadSize() {
		return payloadSize;
	}
	/**
	 * @param payloadSize the payloadSize to set
	 */
	public void setPayloadSize(String payloadSize) {
		this.payloadSize = payloadSize;
	}
	/**
	 * @return the serviceName
	 */
	public String getServiceName() {
		return serviceName;
	}
	/**
	 * @param serviceName the serviceName to set
	 */
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
}
